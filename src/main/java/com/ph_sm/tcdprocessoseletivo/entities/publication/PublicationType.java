package com.ph_sm.tcdprocessoseletivo.entities.publication;

/**
 * This enum defines the type of a publication
 * NEWS: A news publication
 * ANNOUNCEMENT: An announcement publication
 * INSTRUCTIONS: A publication for general guidance
 * EXAM: An exam publication
 * ANSWER_KEY: An answer key publication for an exam
 */
public enum PublicationType {
    NEWS,
    ANNOUNCEMENT,
    INSTRUCTIONS,
    EXAM,
    ANSWER_KEY
}
